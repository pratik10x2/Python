#Escape sequence characters
print("Hello\nWorld!")         # Newline
print("Hello\tWorld!")         # Horizontal Tab
print("This is a backslash: \\") # Backslash
print("He said, \"Hello!\"")    # Double Quote
print('It\'s a sunny day.')     # Single Quote
print("Line 1\rLine 2")         # Carriage Return
print("Hello\bWorld")           # Backspace
print("Hello\fWorld")           # Form Feed
print("Hello\vWorld")           # Vertical Tab
print("Alert sound\a")          # Bell/Alert
print("Hex: \x48\x65\x6c\x6c\x6f") # Hexadecimal
print("Octal: \110\145\154\154\157") # Octal
print("Unicode: \u0048\u0065\u006c\u006c\u006f") # Unicode (16-bit)
print("Unicode: \U0001F600")    # Unicode (32-bit)
